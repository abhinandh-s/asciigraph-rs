use std::f64::consts::PI;
use asciigraph::{plot, Config};

fn main() {
    let data: Vec<f64> = (0..105)
        .map(|i| {
            let i = i as f64;

            15.0 * (i * ((PI * 4.0) / 120.0)).sin()
        }).collect();

    let graph = plot(&data, Config::default().height(10));

    println!("{graph}");

    // Output:
    //   15.00 ┤          ╭────────╮                                                  ╭────────╮
    //   12.00 ┤       ╭──╯        ╰──╮                                            ╭──╯        ╰──╮
    //    9.00 ┤    ╭──╯              ╰─╮                                       ╭──╯              ╰─╮
    //    6.00 ┤  ╭─╯                   ╰──╮                                  ╭─╯                   ╰──╮
    //    3.00 ┤╭─╯                        ╰─╮                              ╭─╯                        ╰─╮
    //    0.00 ┼╯                            ╰╮                            ╭╯                            ╰╮
    //   -3.00 ┤                              ╰─╮                        ╭─╯                              ╰─╮
    //   -6.00 ┤                                ╰─╮                   ╭──╯                                  ╰─╮
    //   -9.00 ┤                                  ╰──╮              ╭─╯                                       ╰──╮
    //  -12.00 ┤                                     ╰──╮        ╭──╯                                            ╰──╮
    //  -15.00 ┤                                        ╰────────╯                                                  ╰───
}