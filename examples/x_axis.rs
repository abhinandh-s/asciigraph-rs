use asciigraph::{plot, Config};
fn main() {
    let data: Vec<f64> = vec![3.0, 4.0, 9.0, 6.0, 2.0, 4.0, 5.0, 8.0, 5.0, 10.0, 2.0, 7.0, 2.0, 5.0, 6.0];
    let graph = plot(
        &data,
        Config::default()
            .x_axis_range(0.0, 14.0)
            .x_axis_tick_count(3)
    );

    println!("{graph}");

    // Output:
    //  10.00 ┤        ╭╮
    //   9.00 ┤ ╭╮     ││
    //   8.00 ┤ ││   ╭╮││
    //   7.00 ┤ ││   ││││╭╮
    //   6.00 ┤ │╰╮  ││││││ ╭
    //   5.00 ┤ │ │ ╭╯╰╯│││╭╯
    //   4.00 ┤╭╯ │╭╯   ││││
    //   3.00 ┼╯  ││    ││││
    //   2.00 ┤   ╰╯    ╰╯╰╯
    //        └┬──────┬──────┬
    //         0      7     14
}